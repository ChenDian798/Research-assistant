export function normalizeLanguage(value) {
  return value === "en" ? "en" : "zh";
}

export function getInitialLanguage() {
  try {
    return normalizeLanguage(window.localStorage.getItem(storageKey));
  } catch (error) {
    return "zh";
  }
}

export function saveLanguage(value) {
  const language = normalizeLanguage(value);
  try {
    window.localStorage.setItem(storageKey, language);
  } catch (error) {
    // Local storage can be unavailable in restricted browser contexts.
  }
  return language;
}

export function createTranslator(language) {
  const normalized = normalizeLanguage(language);
  const dictionary = translations[normalized] || translations.zh;
  return (key, params = {}) => {
    const entry = dictionary[key] ?? translations.zh[key] ?? key;
    if (typeof entry === "function") return entry(params);
    return String(entry).replace(/\{(\w+)\}/g, (_, name) => params[name] ?? "");
  };
}
